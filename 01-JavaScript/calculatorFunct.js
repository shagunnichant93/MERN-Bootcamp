function add(a,b)
{
    return a + b;
}
function multiply(a,b)
{
    return a * b;
}
function subtract(a,b)
{
    return a - b;
}
function divide(a,b)
{
    return a / b;
}

add(10,5);
console.log("The sum is: " + add(10,5));
multiply(10,5);
console.log("The product is: " + multiply(10,5));
subtract(10,5);
console.log("The difference is: " + subtract(10,5));
divide(10,5);  
console.log("The quotient is: " + divide(10,5));
