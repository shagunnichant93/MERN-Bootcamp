let food = 8;
switch (food) 
{
    case 1:
        console.log("Pizza");
        break;
    case 2:
        console.log("Burger");
        break;
    case 3:
        console.log("Pasta");
        break;
    case 4:
        console.log("Salad");
        break;
    case 5:
        console.log("Sushi");
        break;
    case 6:
        console.log("Tacos");
        break;
    case 7:
        console.log("Noodles");
        break;
    default:
        console.log("Invalid Food Choice");
}